/****************************************************************************************
 * This class encapsulates keyboard mapping.
 ****************************************************************************************/
public class KeyBinding {
    public int Up;
    public int Left;
    public int Right;
    public int Down;
    public int Grapple;
    public KeyBinding(int up, int left, int right, int down, int grapple){
        Up = up;
        Left = left;
        Right = right;
        Down = down;
        Grapple = grapple;
    }
}
